"""
SLD Risk Prediction Benchmark — Step 5 Plots: ROC & Precision-Recall Curves

This script generates ONLY the two discrimination plots specific to Step 5.
All other plots are generated by their respective step scripts:

    Step 6  → step6_calibration.py       → plots/calibration_*.png
    Step 7  → step7_interpretability.py  → plots/shap_bar_*.png
                                           plots/shap_beeswarm_*.png
                                           plots/pdp_*.png
                                           plots/ebm_shape_*.png
    Step 8  → step8_9_benchmark_fairness.py → plots/benchmark_heatmap.png
    Step 9  → step8_9_benchmark_fairness.py → plots/fairness_*.png

Input  : splits.pkl, all_models.pkl
Output : plots/roc_curves.png
         plots/pr_curves.png
"""

import pickle
import warnings
import os
import numpy as np
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
from sklearn.metrics import roc_curve, auc, precision_recall_curve, average_precision_score

warnings.filterwarnings('ignore')

# ── Config ───────────────────────────────────────────────────────────────────
MODELS_PKL = 'all_models.pkl'
SPLITS_PKL = 'splits.pkl'
PLOTS_DIR  = 'plots'

MODELS  = ['LogisticRegression', 'EBM', 'RandomForest', 'LightGBM']
TARGETS = ['y_steatosis', 'y_fibrosis']
COLORS  = {
    'LogisticRegression': '#2196F3',
    'EBM':                '#FF9800',
    'RandomForest':       '#4CAF50',
    'LightGBM':           '#9C27B0',
}

os.makedirs(PLOTS_DIR, exist_ok=True)

results     = pickle.load(open(MODELS_PKL, 'rb'))
splits_data = pickle.load(open(SPLITS_PKL, 'rb'))
splits      = splits_data['splits']

# ── ROC Curves ───────────────────────────────────────────────────────────────
print("Generating ROC curves...", flush=True)
fig, axes = plt.subplots(1, 2, figsize=(13, 5))
for ax, target in zip(axes, TARGETS):
    s = splits[target]
    X_test, y_test = s['X_test'], s['y_test']
    for mname in MODELS:
        proba = results[target][mname]['model'].predict_proba(X_test)[:, 1]
        fpr, tpr, _ = roc_curve(y_test, proba)
        ax.plot(fpr, tpr, color=COLORS[mname], lw=2,
                label=f'{mname} (AUC={auc(fpr, tpr):.3f})')
    ax.plot([0, 1], [0, 1], 'k--', lw=1, label='Random')
    ax.fill_between([0, 1], [0, 1], alpha=0.05, color='gray')
    ax.set_xlim(0, 1); ax.set_ylim(0, 1.01)
    ax.set_xlabel('False Positive Rate', fontsize=11)
    ax.set_ylabel('True Positive Rate', fontsize=11)
    ax.set_title(f'ROC Curves — {target}', fontsize=12, fontweight='bold')
    ax.legend(loc='lower right', fontsize=8)
    ax.grid(alpha=0.3)

plt.tight_layout()
plt.savefig(f'{PLOTS_DIR}/roc_curves.png', dpi=150, bbox_inches='tight')
plt.close()
print(f"  ✅ {PLOTS_DIR}/roc_curves.png")

# ── Precision-Recall Curves ───────────────────────────────────────────────────
print("Generating Precision-Recall curves...", flush=True)
fig, axes = plt.subplots(1, 2, figsize=(13, 5))
for ax, target in zip(axes, TARGETS):
    s = splits[target]
    X_test, y_test = s['X_test'], s['y_test']
    baseline = y_test.mean()
    ax.axhline(baseline, color='gray', linestyle='--', lw=1,
               label=f'Baseline (prev={baseline:.2f})')
    for mname in MODELS:
        proba = results[target][mname]['model'].predict_proba(X_test)[:, 1]
        prec, rec, _ = precision_recall_curve(y_test, proba)
        ap = average_precision_score(y_test, proba)
        ax.plot(rec, prec, color=COLORS[mname], lw=2,
                label=f'{mname} (AP={ap:.3f})')
    ax.set_xlim(0, 1); ax.set_ylim(0, 1.01)
    ax.set_xlabel('Recall', fontsize=11)
    ax.set_ylabel('Precision', fontsize=11)
    ax.set_title(f'Precision-Recall Curves — {target}', fontsize=12, fontweight='bold')
    ax.legend(loc='upper right', fontsize=8)
    ax.grid(alpha=0.3)

plt.tight_layout()
plt.savefig(f'{PLOTS_DIR}/pr_curves.png', dpi=150, bbox_inches='tight')
plt.close()
print(f"  ✅ {PLOTS_DIR}/pr_curves.png")

print("\n✅ Step 5 plots done")
